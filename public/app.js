      if (!userData) return;

      // –ü–æ–ª—É—á–∏–º –∞–∫—Ç—É–∞–ª—å–Ω—ã–π —Å–ø–∏—Å–æ–∫ –∑–∞–¥–∞—á (–∏–ª–∏ –º–æ–∂–Ω–æ —Ö—Ä–∞–Ω–∏—Ç—å –∫—ç—à –≤ –≥–ª–æ–±–∞–ª—å–Ω–æ–π –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π)
      const tasksRes = await fetch(`/api/tasks?email=${encodeURIComponent(getCurrentUser())}`);
      const data = await tasksRes.json();
      const originalTask = data.tasks.find(t => t.id == taskId); // == —Ç.–∫. id –º–æ–∂–µ—Ç –±—ã—Ç—å —Å—Ç—Ä–æ–∫–æ–π –∏–ª–∏ —á–∏—Å–ª–æ–º

      if (!originalTask) return;

