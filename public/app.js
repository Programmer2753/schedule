      // –î–ª—è –ø—Ä–æ—Å—Ç–æ—Ç—ã, –¥–∞–≤–∞–π —Å–¥–µ–ª–∞–µ–º –±—ã—Å—Ç—Ä—ã–π fetch —Å–ø–∏—Å–∫–∞, –Ω–∞–π–¥–µ–º –∑–∞–¥–∞—á—É –∏ —Å–∫–æ–ø–∏—Ä—É–µ–º.
      
